#include <iostream>

using namespace std;

int main() {
    
    int a;
    cout << "size of int : " << sizeof(a) << endl;
    
    float b;
    cout << "size of float : " << sizeof(b) << endl;
    
    char c;
    cout << "size of character : " << sizeof(c) << endl;
    
    bool d;
    cout << "size of boolean : " << sizeof(d) << endl;
    
    return 0;
}
